#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const sources = [
  resolve(root, 'src-tauri/src/error.rs'),
  resolve(root, 'src-tauri/src/search_engine/mod.rs'),
];

const kindRegex = /=>\s*"([a-z][a-z0-9_]*)"/g;
const harvested = new Set();

const tsOnlyKinds = [
  'uncaught_exception',
  'unhandled_rejection',
  'render_error',
  'invoke_unknown',
  'extension_proxy_error',
  'extension_crash',
  'iframe_uncaught',
  'iframe_unhandled_rejection',
  'rpc_timeout',
  'panic',
  'manual',
  'action_failed',
];

for (const path of sources) {
  const text = readFileSync(path, 'utf8');
  const blockMatch = text.match(/fn kind\(&self\) -> &'static str \{[\s\S]*?\n\s*\}\n\s*\}/);
  if (!blockMatch) continue;
  for (const m of blockMatch[0].matchAll(kindRegex)) {
    harvested.add(m[1]);
  }
}

for (const k of tsOnlyKinds) harvested.add(k);

const sorted = [...harvested].sort();
const out = `// AUTO-GENERATED by scripts/generate-diagnostic-kinds.mjs — do not edit.
// Run \`pnpm gen:diagnostic-kinds\` after changing AppError or SearchError.

export const DIAGNOSTIC_KINDS = ${JSON.stringify(sorted, null, 2)} as const;

export type DiagnosticKind = typeof DIAGNOSTIC_KINDS[number];
`;

const outPath = resolve(root, 'src/services/diagnostics/kinds.ts');
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(`wrote ${outPath} with ${sorted.length} kinds`);
